    @Override
    public void validateInput() throws Exception {
        if (fieldNama.getText().isEmpty()) {
            throw new Exception("Field nama harus diisi");
        }
        
        try {
            positionGroup.getSelection().getActionCommand();
        } catch (Exception e) {
            throw new Exception("Pilih posisi");
        }
    }
