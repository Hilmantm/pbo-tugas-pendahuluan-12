    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            validateInput();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    null, 
                    e.getMessage(), 
                    "Failed", 
                    JOptionPane.ERROR_MESSAGE);
        }
        
        int tahun = Integer.parseInt(fieldTahun.getSelectedItem().toString());
        int bulan = Integer.parseInt(fieldBulan.getSelectedItem().toString());
        int tanggal = Integer.parseInt(fieldTanggal.getSelectedItem().toString());
        Date ttl = Date.valueOf(String.format("%d-%d-%d", tahun, bulan, tanggal));
        
        Pegawai newPegawai = new Pegawai();
        newPegawai.setNama(fieldNama.getText());
        newPegawai.setPosisi(positionGroup.getSelection().getActionCommand());
        newPegawai.setTglLahir(ttl);
        
        if (this.type == EDIT_TYPE) {
            newPegawai.setId(selectedPegawai.getId());
        }
        
        callback.save(newPegawai, this);
    }//GEN-LAST:event_btnSaveActionPerformed

