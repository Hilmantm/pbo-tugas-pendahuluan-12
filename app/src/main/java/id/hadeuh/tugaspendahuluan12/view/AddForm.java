    private void prepareLayout() {
        fieldStaff.setActionCommand(App.POSISI_STAFF);
        fieldManager.setActionCommand(App.POSISI_MANAGER);
        
        if (this.type == EDIT_TYPE) { 
           fieldNama.setText(selectedPegawai.getNama());
           
           if (selectedPegawai.getPosisi().equals(App.POSISI_STAFF)) {
               fieldStaff.setSelected(true);
           } else {
               fieldManager.setSelected(true);
           }
           
           String[] ttl = selectedPegawai.getTglLahir().toString().split("-");
           fieldTahun.setSelectedItem(ttl[0]);
           fieldBulan.setSelectedItem(ttl[1]);
           fieldTanggal.setSelectedItem(ttl[2]);
        }
    }

